
import { useState } from "react";
import ReportSettings from "../components/ReportingGeneration";
import ScheduledReport from "./ScheduledReport";

const SUB_TABS = [
  { id: "report-generating", label: "Report Generating Setting" },
  { id: "scheduled-reports", label: "Scheduled Reports" },
];

export default function ReportingAndScheduled() {
  const [activeSubTab, setActiveSubTab] = useState("report-generating");

  return (
    <div className="min-h-screen p-8">
      <div className="max-w-[1500px] mx-auto">
        <div className="flex items-center gap-10 mb-6">
          {SUB_TABS.map((tab) => (
            <button
              key={tab.id}
              onClick={() => setActiveSubTab(tab.id)}
              className={`pb-1 ${
                activeSubTab === tab.id
                  ? "text-blue-600 font-medium border-b-2 border-blue-600"
                  : "text-gray-500 hover:text-gray-700"
              }`}
            >
              {tab.label}
            </button>
          ))}
        </div>
        {activeSubTab === "scheduled-reports" ? (
          <ScheduledReport />
        ) : (
          <ReportSettings />
        )}
      </div>
    </div>
  );
}

